package day05_variables;

public class BooleanExample {
    public static void main (String [] args){

        boolean isSleepy = true; // you can not use capital letter for true or false.
        boolean isHungry = false; // you can not use capital letter for true or false.
        boolean isWeekend = false; // you can not use Yes or No
        boolean isWeekday = true; // you can not use 1 or 0

        System.out.println("isSleepy = " + isSleepy);
        System.out.println("isHungry = " + isHungry);
        System.out.println("It is a Weekend = " + isWeekend);
        System.out.println("It is a Weekday = " + isWeekday);


    }
}
