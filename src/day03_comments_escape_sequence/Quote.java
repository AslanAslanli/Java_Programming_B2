package day03_comments_escape_sequence;

public class Quote {
    public static void main (String [] args){

        // how to print out " (double quotation)? - by adding back
        System.out.println("I like \"Java\" programming");

        //How to print out single quotation? - it is included in the syntax
        System.out.println("I like 'Java' programming");

        //How to print out backward slash itself? - by adding another backward slash
        System.out.println("I like backward slash \\");

        //How to print out two backward slashes? - add another bacwards slash for each one
        System.out.println("I like backward slash \\\\");
    }
}
