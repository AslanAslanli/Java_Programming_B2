package day18_loops;

public class BranchingExample {
    public static void main(String[] args) {

        System.out.println("----------------------");

        for (int i = 0; i <= 10; i++) {
            System.out.println("godosh");
            if (i == 5) {
                continue;
            }
            System.out.println(i + "");
        }




    }
}
