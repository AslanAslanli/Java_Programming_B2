package day07_relational_operators;

public class OperatorPractise2 {
    public static void main(String[] args) {

        int a = 40;
        System.out.println(--a); // a = 39 | output 39
        System.out.println(--a); // a = 38 | output 38
        System.out.println(a--); // output = 38 | a = 37
        System.out.println(--a); // a = 36 | output = 36
        System.out.println(--a); // a = 35 | output = 35
        System.out.println(a--); // output = 35 | a = 34


        System.out.println(a); // output = 34


    }
}
